# ── DOCX 파일 ─────────────────────────────────────────────────────────────────
def make_docx(filename, title, body):
    doc = Document()
    doc.add_heading(title, level=1)
    for line in body.strip().split("\n"):
        doc.add_paragraph(line)
    doc.save(OUTPUT / filename)
    print(f"생성: {filename}")

